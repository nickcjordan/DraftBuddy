package com.falifa.draftbuddy.ui.model.player;

public class OffensiveLineMetadata {
	
	private String rank;
	private String passingScore;
	private String rushingScore;
	private String avgScore;
	
	public String getRank() {
		return rank;
	}
	public void setRank(String rank) {
		this.rank = rank;
	}
	public String getPassingScore() {
		return passingScore;
	}
	public void setPassingScore(String passingScore) {
		this.passingScore = passingScore;
	}
	public String getRushingScore() {
		return rushingScore;
	}
	public void setRushingScore(String rushingScore) {
		this.rushingScore = rushingScore;
	}
	public String getAvgScore() {
		return avgScore;
	}
	public void setAvgScore(String avgScore) {
		this.avgScore = avgScore;
	}
	
}
