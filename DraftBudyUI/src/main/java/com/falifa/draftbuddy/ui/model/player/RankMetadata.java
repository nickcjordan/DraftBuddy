package com.falifa.draftbuddy.ui.model.player;

public class RankMetadata {

	private String overallRank;
	private String positionRank;
	private String bestRank;
	private String worstRank;
	private String avgRank;
	private String stdDev;
	private String adp;
	private String vsAdp;
	
	public String getOverallRank() {
		return overallRank;
	}
	public void setOverallRank(String overallRank) {
		this.overallRank = overallRank;
	}
	public String getPositionRank() {
		return positionRank;
	}
	public void setPositionRank(String positionRank) {
		this.positionRank = positionRank;
	}
	public String getBestRank() {
		return bestRank;
	}
	public void setBestRank(String bestRank) {
		this.bestRank = bestRank;
	}
	public String getWorstRank() {
		return worstRank;
	}
	public void setWorstRank(String worstRank) {
		this.worstRank = worstRank;
	}
	public String getAvgRank() {
		return avgRank;
	}
	public void setAvgRank(String avgRank) {
		this.avgRank = avgRank;
	}
	public String getStdDev() {
		return stdDev;
	}
	public void setStdDev(String stdDev) {
		this.stdDev = stdDev;
	}
	public String getAdp() {
		return adp;
	}
	public void setAdp(String adp) {
		this.adp = adp;
	}
	public String getVsAdp() {
		return vsAdp;
	}
	public void setVsAdp(String vsAdp) {
		this.vsAdp = vsAdp;
	}
	
}
