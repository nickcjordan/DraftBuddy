package com.falifa.draftbuddy.ui.tracker.model;

public class MockDraftPick {
	
	private int pickNumber;
	private String playerName;
	private String playerReferenceId;
	
	public int getPickNumber() {
		return pickNumber;
	}
	public void setPickNumber(int pickNumber) {
		this.pickNumber = pickNumber;
	}
	public String getPlayerName() {
		return playerName;
	}
	public void setPlayerName(String playerName) {
		this.playerName = playerName;
	}
	public String getPlayerReferenceId() {
		return playerReferenceId;
	}
	public void setPlayerReferenceId(String playerReferenceId) {
		this.playerReferenceId = playerReferenceId;
	}
	
}
